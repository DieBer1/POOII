    private void ConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConsultarActionPerformed
        // TODO add your handling code here:
        boolean a = false;
        for(i=0;i<=contador;i++)
        {
            if(datosVehiculo[i][0].equals(placa.getText()))
            {
            a = true;
            JOptionPane.showMessageDialog(null,"Existe el vehiculo guardado con esta placa"
                    + "placa = "+datosVehiculo[i][0]
                    + "modelo = "+datosVehiculo[i][1]
                    + "Marca = "+datosVehiculo[i][2]
                    + "Due√±o = "+datosPropietario[i][0]+" "+datosPropietario[i][1]
                    + "Vehiculo numero = "+datosVehiculo[i][3]
                    + "IMPORTANTE: Recuerde el numero de su vehiculo en caso de querer actualizar");
            }
        }
            if(a==false)
            {
                JOptionPane.showMessageDialog(null,"El vehiculo con esta placa no existe");
            }
        
    }//GEN-LAST:event_ConsultarActionPerformed

